interface CircularProgressProps {
  percentage: number
  size?: number
  strokeWidth?: number
  color?: string
  className?: string
  showPercentage?: boolean // new prop
}

export function CircularProgress({
  percentage,
  size = 40,
  strokeWidth = 3,
  color = "hsl(var(--blue-primary))",
  className = "",
  showPercentage = true, // default true
}: CircularProgressProps) {
  const radius = (size - strokeWidth) / 2
  const circumference = radius * 2 * Math.PI
  const strokeDasharray = circumference
  const strokeDashoffset = circumference - (percentage / 100) * circumference

  return (
    <div className={`relative inline-flex items-center justify-center ${className}`}>
      <svg width={size} height={size} className="transform -rotate-90">
        <circle
          cx={size / 2}
          cy={size / 2}
          r={radius}
          stroke="hsl(var(--border))"
          strokeWidth={strokeWidth}
          fill="transparent"
        />
        <circle
          cx={size / 2}
          cy={size / 2}
          r={radius}
          stroke={color}
          strokeWidth={strokeWidth}
          fill="transparent"
          strokeDasharray={strokeDasharray}
          strokeDashoffset={strokeDashoffset}
          strokeLinecap="round"
          className="transition-all duration-300 ease-in-out"
        />
      </svg>

      {/* Only show percentage if showPercentage is true */}
      {showPercentage && (
        <span className="absolute text-xs font-medium text-white">{percentage}%</span>
      )}
    </div>
  )
}
